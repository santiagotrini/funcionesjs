const max = require("../max");

test("max([-8, 5, 3]) to be 5", () => {
  expect(max([-8, 5, 3])).toBe(5);
});

test("max([7842787, 198198298, 300000000]) to be 300000000", () => {
  expect(max([784277, 198198298, 300000000])).toBe(300000000);
});

test("max([12, 66, -1, 0, 1, -99, 1]) to be 66", () => {
  expect(max([12, 66, -1, 0, 1, -99, 1])).toBe(66);
});
